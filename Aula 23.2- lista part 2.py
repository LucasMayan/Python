lista = [124, 4 ,54543, 7575, 6 , 7, 8, 0, 24324]

lista.sort() # altera a ordem e da variavel e coloca em sequencia alfanumerica crescente
print(lista)

sorted(lista) # não altera a ordem
print(lista)

lista.sort(reverse=True) # altera a ordem e da variavel e coloca em sequencia alfanumerica decrecente
print(lista)

lista.reverse() # altera a ordem alfanumerica da varivel fazendo começar de trás para frente
print(lista)

